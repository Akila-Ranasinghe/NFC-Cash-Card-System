    private void loadCustomers() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) Table_customers.getModel();
            ResultSet rsCust = JDBC.getData("SELECT * FROM customer ORDER BY f_name ");
            dtm.setRowCount(0);
            while (rsCust.next()) {
                Vector v = new Vector();
                v.add(rsCust.getString("customer_id"));
                v.add(rsCust.getString("f_name") + " " + rsCust.getString("l_name"));
                if (rsCust.getString("status").equals("Active")) {
                    v.add("<html><font color='Green'>Active</font></html>");
                } else {
                    v.add("<html><font color='Red'>Deactive</font></html>");
                }
                v.add(rsCust.getString("status"));
                dtm.addRow(v);
            }

            // set Action buttton
            btn_activate.setVisible(false);
            btn_deactivate.setVisible(false);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void updateCustomerStatus(String newStatus) {
        try {
            JDBC.putData(" UPDATE customer SET status = '" + newStatus + "'  WHERE customer_id= '" + Table_customers.getValueAt(Table_customers.getSelectedRow(), 0).toString() + "'  ");
            btn_refresh.doClick();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
