    private void Table_customersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table_customersMouseClicked

        if (evt.getButton() == MouseEvent.BUTTON1) {
            if (Table_customers.getSelectedRow() != -1) {
                if (Table_customers.getValueAt(Table_customers.getSelectedRow(), 3).toString().equals("Active")) {
                    btn_activate.setVisible(false);
                    btn_deactivate.setVisible(true);
                } else {
                    btn_activate.setVisible(true);
                    btn_deactivate.setVisible(false);
                }
            } else {
                btn_activate.setVisible(false);
                btn_deactivate.setVisible(false);
            }
        }

    }//GEN-LAST:event_Table_customersMouseClicked

