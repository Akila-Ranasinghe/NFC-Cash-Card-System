    private void openSerialPORTCommService() {
        try {

            //Communicator object
            communicator = new Communicator();

            // search for  available PORT
            String portName = communicator.searchForPorts();
           // System.out.println("FOUND PORTNAME: " + portName);

            // Connect to available PORT + init PORT Listener
            communicator.connect(portName);
            if (communicator.getConnected() == true) {
                if (communicator.initIOStream() == true) {
                    communicator.initListener();
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "NFC Device Not Found.\nPlease check NFC device connection first.", "Communication Faild", 0);
                System.exit(0);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

