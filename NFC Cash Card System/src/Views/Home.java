    private void btn_exitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_exitActionPerformed

        try {
            int i = JOptionPane.showConfirmDialog(rootPane, "By exiting, Cash Card System will be stopped until your next login.\nAre you sure, you want to exit now ?", "Warning", 0);
            if (i == 0) {
                // ADmin Password Confirmation 4 Exit
                JPasswordField pswField = new JPasswordField();
                int confirmPSW = JOptionPane.showConfirmDialog(rootPane, pswField, "Please Insert Administrator Password", JOptionPane.OK_CANCEL_OPTION);
                if (confirmPSW == 0) { // if yes
                    if (!pswField.getText().toString().trim().isEmpty()) {
                        ResultSet rs = JDBC.getData("SELECT id, password FROM admin WHERE id='" + adminUserID + "' AND password = '" + pswField.getText().toString().trim() + "' ");
                        if (rs.next()) {
                            communicator.disconnect();
                            System.exit(0);
                        } else {
                            JOptionPane.showMessageDialog(rootPane, "Invalid Password.", "Access Denied", 2);
                        }
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Please Insert Valid Password.", "Access Denied", 2);
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_exitActionPerformed

    private void btnCust_manageAccActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCust_manageAccActionPerformed

        try {
            // ADmin Password Confirmation
            JPasswordField pswField = new JPasswordField();
            int confirmPSW = JOptionPane.showConfirmDialog(rootPane, pswField, "Please Insert Administrator Password", JOptionPane.OK_CANCEL_OPTION);
            if (confirmPSW == 0) { // if yes
                if (!pswField.getText().toString().trim().isEmpty()) {
                    ResultSet rs = JDBC.getData("SELECT id, password FROM admin WHERE id='" + adminUserID + "' AND password = '" + pswField.getText().toString().trim() + "' ");
                    if (rs.next()) {
                        new Customer_ManageAccs(this, true).setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Invalid Password.", "Access Denied", 2);
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Please Insert Valid Password.", "Access Denied", 2);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnCust_manageAccActionPerformed

