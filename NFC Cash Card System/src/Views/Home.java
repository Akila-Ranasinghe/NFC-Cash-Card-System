    private void uiConfig() {
        try {
            // check Customer Account already exist OR not 
            ResultSet rs1 = JDBC.getData(" SELECT customer_id FROM customer ");
            if (rs1.next()) {
                btnCust_accDetails.setEnabled(true);
                btnCust_reload.setEnabled(true);
                btnCust_manageAcc.setEnabled(true);
            } else {
                btnCust_accDetails.setEnabled(false);
                btnCust_reload.setEnabled(false);
                btnCust_manageAcc.setEnabled(false);
            }

            // check  Sellers Account already exist OR not 
            ResultSet rs2 = JDBC.getData(" SELECT payee_account_id FROM payee_account ");
            if (rs2.next()) {
                btnSeller_accDetails.setEnabled(true);
            } else {
                btnSeller_accDetails.setEnabled(false);
            }

            lbl_deviceOn.setVisible(false);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void frameExitingPrompt() {
        this.setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);
        addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent evt) {
                try {
                    int i = JOptionPane.showConfirmDialog(rootPane, "By exiting, Cash Card System will be stopped until your next login.\nAre you sure, you want to exit now ?", "Warning", 0);
                    if (i == 0) {
                        // ADmin Password Confirmation 4 Exit
                        JPasswordField pswField = new JPasswordField();
                        int confirmPSW = JOptionPane.showConfirmDialog(rootPane, pswField, "Please Insert Administrator Password", JOptionPane.OK_CANCEL_OPTION);
                        if (confirmPSW == 0) { // if yes
                            if (!pswField.getText().toString().trim().isEmpty()) {
                                ResultSet rs = JDBC.getData("SELECT id, password FROM admin WHERE id='" + adminUserID + "' AND password = '" + pswField.getText().toString().trim() + "' ");
                                if (rs.next()) {
                                    communicator.disconnect();
                                    System.exit(0);
                                } else {
                                    JOptionPane.showMessageDialog(rootPane, "Invalid Password.", "Access Denied", 2);
                                }
                            } else {
                                JOptionPane.showMessageDialog(rootPane, "Please Insert Valid Password.", "Access Denied", 2);
                            }
                        }
                    }
                } catch (Exception e) {
                }
            }
        }
        );
    }

