    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed

        try {
            // check alll fields are filled
            if (comboBox_accName.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Please select account name which you want to access");
                comboBox_accName.showPopup();
            } else {
                // load AccNames
                ResultSet r = JDBC.getData("SELECT payee_account_id,account_name, password FROM payee_account WHERE account_name = '" + comboBox_accName.getSelectedItem().toString() + "'  ");
                if (r.next()) {
                    this.dispose();
                    new Seller_AccountDetails(null, true, r.getString("payee_account_id").toString(), r.getString("account_name").toString()).setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Please insert valid password.", "Invalid Password", 2);
                    comboBox_accName.showPopup();
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_loginActionPerformed

