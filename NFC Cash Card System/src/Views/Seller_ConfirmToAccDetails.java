    private void loadUserAccNames() {
        try {
            comboBox_accName.removeAllItems();
            Vector v = new Vector();
            v.add("              -   Select Here   -");
            ResultSet rSup = JDBC.getData("SELECT account_name FROM payee_account ORDER BY account_name");
            while (rSup.next()) {
                v.add(rSup.getString("account_name"));
            }
            comboBox_accName.setModel(new DefaultComboBoxModel(v));

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
