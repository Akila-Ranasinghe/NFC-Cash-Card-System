    private void calcBalance() {
        try {
            // check withdraw amount is empty OR not
            if (txt_WithdrawAmount.getText().toString() == null | txt_WithdrawAmount.getText().toString().isEmpty()) {
                txt_Balance.setText(txt_AvlAmount.getText());
                btn_withdraw.setEnabled(false);  // Disable Withdraw
                btn_withdraw.setBackground(new Color(240, 240, 240));
            } else {
                // revoke unecessary input values
                if (txt_WithdrawAmount.getText().toString().trim().equals(".")) {
                    btn_withdraw.setEnabled(false);  // Disable Withdraw
                    btn_withdraw.setBackground(new Color(240, 240, 240));
                } else {
                    txt_Balance.setText(String.valueOf(new DecimalFormat("#0.00").format(Double.valueOf(txt_AvlAmount.getText()) - Double.valueOf(txt_WithdrawAmount.getText()))));
                    // Check if Withdraw Amount > Available Amount
                    if (Double.valueOf(txt_WithdrawAmount.getText()) > Double.valueOf(txt_AvlAmount.getText())) {
                        btn_withdraw.setEnabled(false);  // Disable Withdraw
                        btn_withdraw.setBackground(new Color(240, 240, 240));
                    } else {
                        // Check if Withdraw Amount == 0.00
                        if (Double.valueOf(txt_WithdrawAmount.getText()) == 0.00) {
                            btn_withdraw.setEnabled(false);  // Disable Withdraw
                            btn_withdraw.setBackground(new Color(240, 240, 240));
                        } else {
                            btn_withdraw.setEnabled(true);  // Enable Withdraw
                            btn_withdraw.setBackground(new Color(0, 125, 184));
                        }
                    }
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

