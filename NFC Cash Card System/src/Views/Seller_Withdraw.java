    private void btn_withdrawActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_withdrawActionPerformed

        try {
            int i = JOptionPane.showConfirmDialog(rootPane, "Are you sure, you want to withdraw money now ?", "Withdraw Money", 0);
            if (i == 0) {
                // save withdraw record
                JDBC.putData("INSERT INTO withdraw VALUES('0' , '" + payeeAccID + "' , '" + txt_WithdrawAmount.getText() + "' , '" + Home.adminUserID + "' , '" + new SimpleDateFormat("yyyy-MM-dd").format(new Date()).toString() + "' , '" + new SimpleDateFormat("hh:mm:ss a").format(new Date()).toString() + "'   ) ");
                // Update payee account balance amount
                JDBC.putData(" UPDATE payee_account SET balance = '" + txt_Balance.getText() + "' WHERE payee_account_id = '" + payeeAccID + "' ");
                JOptionPane.showMessageDialog(rootPane, "Withdraw Transaction Successful!");
                this.dispose();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btn_withdrawActionPerformed

    private void txt_WithdrawAmountKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_WithdrawAmountKeyTyped

        char key = evt.getKeyChar();
        if (!Character.isDigit(key)) {
            if (key != '.') {
                evt.consume();
            }
        }
        if (key == KeyEvent.VK_PERIOD) {
            if (txt_WithdrawAmount.getText().trim().contains(".")) {
                evt.consume();
            }
        }
        if (txt_WithdrawAmount.getText().length() == 15) {
            evt.consume();
        }

    }//GEN-LAST:event_txt_WithdrawAmountKeyTyped

    private void txt_WithdrawAmountFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txt_WithdrawAmountFocusLost

        final DecimalFormat df = new DecimalFormat("#0.00");
        if (txt_WithdrawAmount.getText().trim().isEmpty()) {
            txt_WithdrawAmount.setText("0");
        }
        if (txt_WithdrawAmount.getText().trim().equals(".")) {
            txt_WithdrawAmount.setText("0");
        }
        txt_WithdrawAmount.setText(df.format(Double.parseDouble(txt_WithdrawAmount.getText().trim())));
        calcBalance();

    }//GEN-LAST:event_txt_WithdrawAmountFocusLost

